from typing import List, Set, Tuple
import copy


class TrieNode:
    def __init__(self, char: str):
        self.char = char
        self.children = dict()
        self.end = False
    
    def __repr__(self) -> str:
        return self.char

class Trie:
    def __init__(self):
        """
        Initialize your data structure here.
        """
        self.root = TrieNode('')

    def insert(self, word: str) -> None:
        """
        Inserts a word into the trie.
        """
        if not word:
            return None
        cur = self.root
        for char in word:
            if char in cur.children:
                cur = cur.children[char]
            else:
                node = TrieNode(char)
                cur.children[char] = node
                cur = node
        cur.end = True

class Solution:
    def findWords(self, board: List[List[str]], words: List[str]) -> List[str]:
        trie = Trie()
        for word in words:
            trie.insert(word)
        res = []
        words = set(words)
        def dfs(i: int, j: int, node: TrieNode, running: str):
            if running in words:
                res.append(running)
            """
            Alternative base-case check:
            if node.end:
                res.append(running)
                node.end = False
            """
            if i < 0 or i >= len(board) or j < 0 or j >= len(board[0]):
                return
            char = board[i][j]
            node = node.children.get(char)
            if not node:
                return 
            board[i][j] = "#"
            dfs(i+1, j, node, running+char)
            dfs(i-1, j, node, running+char)
            dfs(i, j-1, node, running+char)
            dfs(i, j+1, node, running+char)
            board[i][j] = char
        for i in range(len(board)):
            for j in range(len(board[0])):
                dfs(i, j, trie.root, '')
        return list(set(res))

